class GreaterThanOperatorDemo
{
	public static void main(String args[]){
		int a = 29, b = 45,x=10 , y = 10;
		boolean res = a>b;
		boolean res1 = b>a;
		boolean res2 = x>y;
		System.out.println(a+">"+b+"=>"+res);
		System.out.println(b+">"+a+"=>"+res1);
		System.out.println(x+">"+y+"=>"+res2);

	}
}